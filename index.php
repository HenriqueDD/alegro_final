  <script>
    $(function () {
      function load(action) {
        const load_div = $(".loading");
        if (action === "open")
        {
          load_div.fadeIn().css("display", "flex")
        } else {
          load_div.fadeOut()
        }
      }

      function success(action, message) {
        const success_div = $(".sent-message");
        if (action === "open")
        {
          success_div.innerHTML(message)
          success_div.fadeIn().css("display", "flex")
        } else {
          success_div.fadeOut()
        }
      }

      function error(action, message) {
        let error_div = $(".error-message");
        if (action === "open")
        {
          error_div.innerHTML(message)
          error_div.fadeIn().css("display", "flex")
        } else {
          error_div.fadeOut()
        }
      }

      $("#form-contact").submit(function (e) {
        e.preventDefault()

        const form = $(this)

        $.ajax({
          url: 'sendmail.php',
          data: form.serialize(),
          type: "POST",
          dataType: "json",
          beforeSend: function () {
            load("open")
          },
          success: function (callback) {
            if (callback.status === "error") {
              error("open", callback.message)
            } else {
              success("open", callback.message)
            }
          },
          complete: function () {
            load("close")
            success("close")
            error("close")
          }
        })
      })
    })
  </script>

